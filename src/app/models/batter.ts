// model to store the information of a batter in a game
export class Batter {
  name: string; // displayed name of the batter
  ab: string;   // AB of the batter
  r: string;    // runs of the batter
  h: number;    // hits of the batter
  rbi: number;  // RBI of the batter
  bb: string;   // BB of the batter
  so: string;   // strike outs of the batter
  avg: string;  // average of the batter
}